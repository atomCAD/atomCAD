/// Single-atom simple-cubic carbon structure with a tunable lattice parameter.
/// Used for tests where the zincblende motif would obscure the effect of `M`.
fn simple_cubic_carbon(a: f64) -> Structure {
    let lattice_vecs = UnitCellStruct::new(
        DVec3::new(a, 0.0, 0.0),
        DVec3::new(0.0, a, 0.0),
        DVec3::new(0.0, 0.0, a),
    );
    let motif = Motif {
        parameters: vec![],
        sites: vec![Site {
            atomic_number: 6,
            position: DVec3::ZERO,
        }],
        bonds: vec![],
        bonds_by_site1_index: vec![vec![]],
        bonds_by_site2_index: vec![vec![]],
    };
    Structure {
        lattice_vecs,
        motif,
        motif_offset: DVec3::ZERO,
    }
}

/// Face-centered cubic primitive: one atom per cell, with primitive basis
/// vectors that span a conventional cube of side `a`. Applying the standard
/// primitive→conventional matrix gives a 4-atom FCC conventional cell.
fn fcc_primitive(a: f64) -> Structure {
    let half = a * 0.5;
    let lattice_vecs = UnitCellStruct::new(
        DVec3::new(0.0, half, half),
        DVec3::new(half, 0.0, half),
        DVec3::new(half, half, 0.0),
    );
    let motif = Motif {
        parameters: vec![],
        sites: vec![Site {
            atomic_number: 6,
            position: DVec3::ZERO,
        }],
        bonds: vec![],
        bonds_by_site1_index: vec![vec![]],
        bonds_by_site2_index: vec![vec![]],
    };
    Structure {
        lattice_vecs,
        motif,
        motif_offset: DVec3::ZERO,
    }
}

/// Enumerates the real-space (position, atomic_number) pairs generated by the
/// crystal field of `structure` over the cell range `[cell_min, cell_max]`
/// inclusive, then keeps only those whose position lies in the half-open AABB
/// `[aabb_min, aabb_max)`. This is the hand-rolled equivalent of the
/// `materialize` node for a cuboid region — the design doc's "crystal_field"
/// semantics expressed as a concrete finite set.
fn enumerate_crystal_field_in_aabb(
    structure: &Structure,
    cell_min: IVec3,
    cell_max: IVec3,
    aabb_min: DVec3,
    aabb_max: DVec3,
) -> Vec<(DVec3, i16)> {
    let uc = &structure.lattice_vecs;
    let mut atoms = Vec::new();
    for pz in cell_min.z..=cell_max.z {
        for py in cell_min.y..=cell_max.y {
            for px in cell_min.x..=cell_max.x {
                let cell = IVec3::new(px, py, pz).as_dvec3();
                for site in &structure.motif.sites {
                    let frac = cell + structure.motif_offset + site.position;
                    let real = uc.dvec3_lattice_to_real(&frac);
                    if real.x >= aabb_min.x
                        && real.x < aabb_max.x
                        && real.y >= aabb_min.y
                        && real.y < aabb_max.y
                        && real.z >= aabb_min.z
                        && real.z < aabb_max.z
                    {
                        atoms.push((real, site.atomic_number));
                    }
                }
            }
        }
    }
    atoms
}

/// Enumerates bonds in real space as canonically-ordered endpoint pairs. Each
/// bond is produced once per cell in `[cell_min, cell_max]`, endpoint 1 being
/// the site_1 copy in that cell. A bond is kept iff both endpoints lie in
/// `[aabb_min, aabb_max)`.
fn enumerate_bonds_in_aabb(
    structure: &Structure,
    cell_min: IVec3,
    cell_max: IVec3,
    aabb_min: DVec3,
    aabb_max: DVec3,
) -> Vec<(DVec3, DVec3, i32)> {
    let uc = &structure.lattice_vecs;
    let mut bonds = Vec::new();
    for bond in &structure.motif.bonds {
        let s1 = &structure.motif.sites[bond.site_1.site_index];
        let s2 = &structure.motif.sites[bond.site_2.site_index];
        for pz in cell_min.z..=cell_max.z {
            for py in cell_min.y..=cell_max.y {
                for px in cell_min.x..=cell_max.x {
                    let cell = IVec3::new(px, py, pz).as_dvec3();
                    let p1_frac = cell + structure.motif_offset + s1.position;
                    let p2_frac = cell
                        + bond.site_2.relative_cell.as_dvec3()
                        + structure.motif_offset
                        + s2.position;
                    let p1 = uc.dvec3_lattice_to_real(&p1_frac);
                    let p2 = uc.dvec3_lattice_to_real(&p2_frac);
                    let in_box = |p: DVec3| {
                        p.x >= aabb_min.x
                            && p.x < aabb_max.x
                            && p.y >= aabb_min.y
                            && p.y < aabb_max.y
                            && p.z >= aabb_min.z
                            && p.z < aabb_max.z
                    };
                    if in_box(p1) && in_box(p2) {
                        let (a, b) = canonical_pair(p1, p2);
                        bonds.push((a, b, bond.multiplicity));
                    }
                }
            }
        }
    }
    bonds
}

fn canonical_pair(a: DVec3, b: DVec3) -> (DVec3, DVec3) {
    // Lex order on (x, y, z). NaN is impossible for our inputs.
    let a_lt_b = (a.x, a.y, a.z) < (b.x, b.y, b.z);
    if a_lt_b { (a, b) } else { (b, a) }
}

fn sort_atoms(mut atoms: Vec<(DVec3, i16)>) -> Vec<(DVec3, i16)> {
    atoms.sort_by(|x, y| {
        x.0.x
            .partial_cmp(&y.0.x)
            .unwrap()
            .then(x.0.y.partial_cmp(&y.0.y).unwrap())
            .then(x.0.z.partial_cmp(&y.0.z).unwrap())
            .then(x.1.cmp(&y.1))
    });
    atoms
}

fn sort_bonds(mut bonds: Vec<(DVec3, DVec3, i32)>) -> Vec<(DVec3, DVec3, i32)> {
    bonds.sort_by(|x, y| {
        x.0.x
            .partial_cmp(&y.0.x)
            .unwrap()
            .then(x.0.y.partial_cmp(&y.0.y).unwrap())
            .then(x.0.z.partial_cmp(&y.0.z).unwrap())
            .then(x.1.x.partial_cmp(&y.1.x).unwrap())
            .then(x.1.y.partial_cmp(&y.1.y).unwrap())
            .then(x.1.z.partial_cmp(&y.1.z).unwrap())
            .then(x.2.cmp(&y.2))
    });
    bonds
}

fn assert_atom_sets_equal(a: &[(DVec3, i16)], b: &[(DVec3, i16)], tol: f64) {
    assert_eq!(
        a.len(),
        b.len(),
        "atom set sizes differ: {} vs {}",
        a.len(),
        b.len()
    );
    for (i, (pa, pb)) in a.iter().zip(b.iter()).enumerate() {
        assert_eq!(
            pa.1, pb.1,
            "atomic_number differs at index {}: {} vs {}",
            i, pa.1, pb.1
        );
        assert!(
            (pa.0 - pb.0).length() < tol,
            "position differs at index {}: {:?} vs {:?}",
            i,
            pa.0,
            pb.0
        );
    }
}

fn assert_bond_sets_equal(a: &[(DVec3, DVec3, i32)], b: &[(DVec3, DVec3, i32)], tol: f64) {
    assert_eq!(
        a.len(),
        b.len(),
        "bond set sizes differ: {} vs {}",
        a.len(),
        b.len()
    );
    for (i, (ba, bb)) in a.iter().zip(b.iter()).enumerate() {
        assert_eq!(
            ba.2, bb.2,
            "bond multiplicity differs at index {}: {} vs {}",
            i, ba.2, bb.2
        );
        assert!(
            (ba.0 - bb.0).length() < tol,
            "bond endpoint 1 differs at index {}: {:?} vs {:?}",
            i,
            ba.0,
            bb.0
        );
        assert!(
            (ba.1 - bb.1).length() < tol,
            "bond endpoint 2 differs at index {}: {:?} vs {:?}",
            i,
            ba.1,
            bb.1
        );
    }
}

