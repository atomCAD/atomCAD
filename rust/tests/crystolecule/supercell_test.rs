#[test]
fn singular_matrix_returns_degenerate() {
    let s = Structure::diamond();
    let m = [[1, 0, 0], [2, 0, 0], [0, 0, 1]]; // rows 0 and 1 are linearly dependent
    let err = apply_supercell(&s, &m).unwrap_err();
    assert_eq!(err, SupercellError::Degenerate);
}

#[test]
fn diagonal_with_zero_component_returns_degenerate() {
    let s = Structure::diamond();
    let m = [[2, 0, 0], [0, 0, 0], [0, 0, 2]]; // det = 0
    let err = apply_supercell(&s, &m).unwrap_err();
    assert_eq!(err, SupercellError::Degenerate);
}

#[test]
fn negative_determinant_returns_inverted_handedness() {
    let s = Structure::diamond();
    // Swapping two rows flips the sign of det → det = -1.
    let m = [[0, 1, 0], [1, 0, 0], [0, 0, 1]];
    match apply_supercell(&s, &m).unwrap_err() {
        SupercellError::InvertedHandedness { det } => {
            assert_eq!(det, -1);
        }
        other => panic!("expected InvertedHandedness, got {:?}", other),
    }
}

#[test]
fn oversized_supercell_returns_too_large() {
    let s = Structure::diamond(); // 8 sites
    // det = 1_000_000, site count would be 8_000_000 ≫ 10_000.
    let m = [[100, 0, 0], [0, 100, 0], [0, 0, 100]];
    match apply_supercell(&s, &m).unwrap_err() {
        SupercellError::TooLarge { site_count, limit } => {
            assert_eq!(site_count, 8_000_000);
            assert_eq!(limit, MAX_SUPERCELL_SITES);
        }
        other => panic!("expected TooLarge, got {:?}", other),
    }
}
